    id SERIAL PRIMARY KEY,
    title VARCHAR(255) NOT NULL,
    author VARCHAR(255) NOT NULL,
    price DECIMAL(10,2) CHECK (price > 0) NOT NULL,
    genre VARCHAR(100) NOT NULL,
    publication_year INT NOT NULL
);

